void Npc::Update( uint16_t turn )
{
    switch ( turn )
    {
        case DEAL_TURN :
            uint16_t num = hand->GetHandNum();
            if ( num < HAND_MAX )
            {
                hand->Draw( deck->Deal( HAND_MAX - num ) );
            }
            break;
    }
}
